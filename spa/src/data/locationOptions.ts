export function getCountryOption(country: string): string | undefined {
  const normalized = country.trim().toLocaleLowerCase();

  return countryOptions.find(
    (option) => option.toLocaleLowerCase() === normalized,
  );
}

export function getStateOptions(country: string): readonly string[] {
  const selectedCountry = getCountryOption(country);

  return selectedCountry ? stateOptionsByCountry[selectedCountry] ?? [] : [];
}
